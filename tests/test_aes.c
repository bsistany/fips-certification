/*
 * test_aes.c — AES-128/256 CBC test vectors
 *
 * Vectors from FIPS 197 Appendix B and NIST SP 800-38A Appendix F.2
 */

#include <stdio.h>
#include <string.h>
#include "aes.h"

static int tests_run    = 0;
static int tests_passed = 0;

static void check(const char *label, const uint8_t *got,
                  const uint8_t *expected, size_t len) {
    tests_run++;
    if (memcmp(got, expected, len) == 0) {
        printf("  PASS  %s\n", label);
        tests_passed++;
    } else {
        printf("  FAIL  %s\n", label);
        printf("        got:      ");
        for (size_t i = 0; i < len; i++) printf("%02x", got[i]);
        printf("\n        expected: ");
        for (size_t i = 0; i < len; i++) printf("%02x", expected[i]);
        printf("\n");
    }
}

/* ------------------------------------------------------------------ */
/* FIPS 197 Appendix B — AES-128 single block encrypt                 */
/* ------------------------------------------------------------------ */
static void test_fips197_appendix_b(void) {
    const uint8_t key[16] = {
        0x2b,0x7e,0x15,0x16,0x28,0xae,0xd2,0xa6,
        0xab,0xf7,0x15,0x88,0x09,0xcf,0x4f,0x3c
    };
    const uint8_t pt[16] = {
        0x32,0x43,0xf6,0xa8,0x88,0x5a,0x30,0x8d,
        0x31,0x31,0x98,0xa2,0xe0,0x37,0x07,0x34
    };
    const uint8_t expected_ct[16] = {
        0x39,0x25,0x84,0x1d,0x02,0xdc,0x09,0xfb,
        0xdc,0x11,0x85,0x97,0x19,0x6a,0x0b,0x32
    };
    const uint8_t iv[16] = {0};  /* zero IV for single-block CBC == ECB */

    AES_CTX ctx;
    uint8_t ct[16], recovered[16];

    aes_init(&ctx, key, 16);
    aes_cbc_encrypt(&ctx, iv, pt, ct, 16);
    check("AES-128 FIPS197-B encrypt", ct, expected_ct, 16);

    aes_cbc_decrypt(&ctx, iv, ct, recovered, 16);
    check("AES-128 FIPS197-B decrypt", recovered, pt, 16);

    aes_zeroize(&ctx);
}

